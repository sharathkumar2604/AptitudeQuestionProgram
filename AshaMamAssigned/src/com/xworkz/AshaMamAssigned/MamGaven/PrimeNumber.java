public class PrimeNumber {

	public static void main(String[] args) {
		
		int n=5;
		
//		
		for(int i=0;i<=n;i++)
		{
			if(i%2==0)
			{
				System.out.print(i+"even");
			System.out.println();
			}
			else
			{
				System.out.print(i+"not even");
				System.out.println();
			}
		}
		
		
		
		
		
	}
}


